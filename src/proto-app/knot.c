/* Basic types */
#define VTYPE_INT	KNOT_VALUE_TYPE_INT
#define VTYPE_FLOAT	KNOT_VALUE_TYPE_FLOAT
#define VTYPE_BOOL	KNOT_VALUE_TYPE_BOOL
#define VTYPE_RAW	KNOT_VALUE_TYPE_RAW

/* Bitmask to identify used unit */
